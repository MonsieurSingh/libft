#include "libft.h"

t_list	*ft_list_at(t_list *begin_list, size_t nbr)
{
	t_list *current;
	t_list *next;
	size_t	i;

	i = 0;
	current = begin_list;
	while (current)
	{
		next = current->next;
		if (i == nbr)
			return (current);
		current = next;
	}
	return (NULL);
}
